package dao.domain;

import jade.util.leap.Serializable;

import java.sql.Timestamp;

public class Offer implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 8967117310717370213L;
	private int id;
	private int clientId;
	private int annoucementId;
	private Timestamp date;
	private int price;
	
	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public Offer(){}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getClientId() {
		return clientId;
	}
	public void setClientId(int clientId) {
		this.clientId = clientId;
	}
	public int getAnnoucementId() {
		return annoucementId;
	}
	public void setAnnoucementId(int annoucementId) {
		this.annoucementId = annoucementId;
	}
	public Timestamp getDate() {
		return date;
	}
	public void setDate(Timestamp date) {
		this.date = date;
	}
	
}
